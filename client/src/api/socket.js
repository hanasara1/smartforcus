/*
  Socket.IO란?
  HTTP 요청-응답 방식의 axios와 달리, 서버와 클라이언트가
  연결을 유지하며 실시간으로 데이터를 주고받을 수 있는 라이브러리입니다.
  채팅, 알림, 실시간 랭킹 갱신 등 즉각적인 데이터 전달이 필요한 기능에 사용합니다.

  io : Socket.IO 서버에 연결하는 클라이언트 함수입니다.
       호출하면 서버와의 소켓 연결(socket 객체)을 생성하고 반환합니다.
*/
import { io } from 'socket.io-client';


// ────────────────────────────────────────────────
// 🗄️ 소켓 인스턴스 저장소 초기화
// ────────────────────────────────────────────────

/*
  싱글턴 패턴(Singleton Pattern)이란?
  어떤 객체를 앱 전체에서 단 하나만 생성하고, 이후에는 그 하나를 재사용하는 패턴입니다.
  소켓의 경우 컴포넌트가 렌더링될 때마다 새로 생성하면 서버에 중복 연결이 쌓이므로,
  최초 1회만 생성하고 이후에는 동일한 인스턴스를 반환하도록 관리합니다.

  socket 변수는 아래 세 가지 상태를 가질 수 있습니다.
    - null           : 아직 한 번도 연결하지 않은 초기 상태
    - socket 객체    : 연결이 활성화된 상태 (socket.connected === true)
    - socket 객체    : 연결이 끊어졌지만 인스턴스는 남아있는 상태 (socket.connected === false)
*/
let socket = null; // 소켓 인스턴스를 담아두는 변수 (초기값: null)


// ────────────────────────────────────────────────
// 🔌 소켓 연결 초기화 함수
// ────────────────────────────────────────────────

/*
  connectSocket()이 호출될 때 아래 3가지 경우를 순서대로 처리합니다.

  ▼ 처리 흐름 ▼

  경우 1. 이미 연결된 소켓이 있는 경우 (socket?.connected === true)
          → 새 소켓을 만들지 않고 기존 인스턴스를 그대로 반환합니다.
            (중복 연결 방지)

  경우 2. 소켓 인스턴스는 있지만 연결이 끊긴 경우 (socket이 있고 connected === false)
          → socket.connect()로 기존 인스턴스를 재사용하여 재연결합니다.
            새 인스턴스를 만들지 않으므로 이벤트 리스너도 유지됩니다.

  경우 3. 소켓 인스턴스 자체가 없는 경우 (socket === null)
          → io()로 새 소켓 인스턴스를 생성하고 이벤트 리스너를 등록합니다.

  @param {string} token - 서버 인증에 사용할 JWT 액세스 토큰
  @returns 연결된 socket 인스턴스
*/
export const connectSocket = (token) => {

  // ── 경우 1. 이미 연결된 소켓이 있으면 재사용 ───
  // socket?.connected : socket이 null이면 undefined를 반환하므로 TypeError 없이 안전하게 확인합니다.
  if (socket?.connected) return socket;

  // ── 경우 2. 인스턴스는 있지만 연결이 끊긴 경우 ─
  // 기존 인스턴스를 버리지 않고 .connect()로 재연결합니다.
  // 새 인스턴스 생성 시 발생하는 이벤트 리스너 중복 등록 문제를 방지합니다.
  if (socket) {
    socket.connect();
    return socket;
  }

  // ── 경우 3. 소켓 인스턴스 신규 생성 ────────────
  /*
    io(서버 URL, 설정 옵션) : 서버에 소켓 연결을 시도하고 인스턴스를 반환합니다.

    ▼ 설정 옵션 설명 ▼

    - auth                : 연결 시 서버로 전달하는 인증 정보입니다.
                            서버는 token을 검증하여 유효한 유저인지 확인합니다.

    - transports          : 통신 방식을 지정합니다.
                            'websocket'만 사용하면 HTTP polling(긴 폴링) 없이
                            바로 WebSocket으로 연결하여 속도와 안정성이 향상됩니다.

    - reconnectionAttempts: 연결이 끊어졌을 때 자동 재연결을 시도하는 최대 횟수입니다.
                            5회 시도 후에도 실패하면 재연결을 포기합니다.

    - reconnectionDelay   : 재연결 시도 사이의 대기 시간(ms)입니다.
                            1000ms = 1초 간격으로 재연결을 시도합니다.
  */
  socket = io(process.env.REACT_APP_SOCKET_URL || 'http://localhost:5000', {
    auth                : { token },
    transports          : ['websocket'],
    reconnectionAttempts: 5,
    reconnectionDelay   : 1000,
  });

  // ── 연결 오류 이벤트 리스너 등록 ────────────────
  /*
    .off('connect_error').on('connect_error', ...) 패턴 설명

    단순히 .on()만 사용하면 connectSocket()이 여러 번 호출될 경우
    동일한 이벤트에 리스너가 중복 등록되어 오류 로그가 여러 번 출력됩니다.

    .off()로 기존 리스너를 먼저 제거한 뒤 .on()으로 새로 등록하면
    항상 리스너가 하나만 유지됩니다.
      - off(이벤트명) : 해당 이벤트의 모든 리스너를 제거합니다.
      - on(이벤트명, 콜백) : 새 리스너를 등록합니다.
      - 메서드 체이닝 : off()와 on()을 이어서 한 줄로 작성합니다.
  */
  socket.off('connect_error').on('connect_error', (err) => {
    console.error('소켓 연결 오류:', err.message); // 연결 실패 원인을 콘솔에 출력합니다
  });

  return socket; // 생성된 소켓 인스턴스를 반환합니다
};


// ────────────────────────────────────────────────
// 🔌 소켓 연결 해제 함수
// ────────────────────────────────────────────────

/*
  소켓 연결을 종료하고 인스턴스를 초기화합니다.
  로그아웃 또는 앱 종료 시 호출하여 서버 측 연결 리소스를 해제합니다.

  socket을 null로 초기화하는 이유 :
    disconnect() 후에도 인스턴스가 남아있으면 다음 connectSocket() 호출 시
    '경우 2'로 분기되어 기존 인스턴스를 재사용하게 됩니다.
    완전히 새로운 연결이 필요한 경우(예: 다른 계정으로 재로그인)를 위해
    null로 초기화하여 '경우 3'으로 분기되도록 합니다.
*/
export const disconnectSocket = () => {
  if (socket) {
    socket.disconnect(); // 서버와의 소켓 연결을 종료합니다
    socket = null;       // 인스턴스를 초기화하여 다음 연결 시 새로 생성하도록 합니다
  }
};


// ────────────────────────────────────────────────
// 📖 소켓 인스턴스 반환 함수
// ────────────────────────────────────────────────

/*
  현재 생성된 소켓 인스턴스를 반환합니다.
  컴포넌트나 훅(hook)에서 소켓 이벤트를 등록하거나
  메시지를 전송할 때 이 함수로 인스턴스를 가져와 사용합니다.

  소켓이 아직 연결되지 않은 경우 null을 반환하므로,
  호출부에서 null 여부를 확인한 후 사용하는 것을 권장합니다.
  예) const socket = getSocket();
      if (socket) socket.emit('message', data);
*/
export const getSocket = () => socket;
