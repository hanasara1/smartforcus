/*
  axiosInstance란?
  baseURL, 토큰 자동 첨부, 401 자동 로그아웃 처리 등
  공통 설정이 미리 적용된 커스텀 axios 객체입니다.
  (자세한 설정 내용은 src/api/axiosInstance.js 참고)
*/
import axiosInstance from './axiosInstance';


// ────────────────────────────────────────────────
// 🎞️ 타임랩스 API 함수 정의
// ────────────────────────────────────────────────

/*
  ▼ 공통 구조 설명 ▼

  각 함수는 axiosInstance의 HTTP 메서드를 호출하고
  서버 응답(Promise)을 그대로 반환합니다.
  호출부에서 await 또는 .then()으로 응답 결과를 받아야 합니다.

  ▼ 사용하는 HTTP 메서드 ▼
    - POST : 새로운 데이터를 '생성'할 때 사용합니다.
             (예: 타임랩스 파일명 등록)
    - GET  : 서버에서 데이터를 '조회'할 때 사용합니다. (서버 데이터 변경 없음)

  ▼ 파일 자체를 전송하지 않는 이유 ▼
    일반적으로 파일 업로드는 multipart/form-data 형식으로 파일 바이너리를
    직접 서버에 전송합니다. 그러나 이 API는 파일 바이너리 대신
    파일명(문자열)만 서버로 전달합니다.

    이 방식은 클라이언트가 파일을 S3 등 외부 스토리지에 직접 업로드한 뒤,
    저장된 파일의 이름(경로)만 서버 DB에 등록하는 구조일 때 사용합니다.
      - 파일 전송 부담이 서버가 아닌 외부 스토리지로 분산됩니다.
      - 서버는 파일명만 DB에 저장하므로 처리가 가볍습니다.

  ▼ 엔드포인트 설명 ▼
    - '/timelapses'            : 타임랩스 파일명 등록 엔드포인트
                                 imm_idx와 file_name을 요청 본문(body)으로 전달합니다.
    - '/timelapses/${imm_idx}' : 특정 세션의 타임랩스 목록 조회 엔드포인트
                                 imm_idx를 URL 파라미터로 전달하여
                                 해당 세션에 속한 타임랩스만 조회합니다.
*/

// ── 타임랩스 파일명 등록 API ─────────────────────
// 특정 몰입 세션에 타임랩스 파일명을 등록합니다.
// @param {number} imm_idx  - 타임랩스가 속한 몰입 세션의 고유 식별자(ID)
// @param {string} fileName - 외부 스토리지에 저장된 타임랩스 파일명
//   파일 바이너리가 아닌 파일명(문자열)만 전송하므로 요청이 가볍습니다.
//   서버는 이 파일명을 DB에 저장하여 이후 조회 시 파일 경로로 활용합니다.
//   요청 본문 예시 : { imm_idx: 42, file_name: 'timelapse_20260416_093000.mp4' }
// 반환 예시 : { success: true, message: '타임랩스 등록 완료', timelapse_idx: 7, ... }
export const uploadTimelapseAPI = (imm_idx, fileName) => {
  return axiosInstance.post('/timelapses', { imm_idx, file_name: fileName });
};

// ── 타임랩스 목록 조회 API ───────────────────────
// 특정 몰입 세션에 등록된 타임랩스 파일 목록을 가져옵니다.
// @param {number} imm_idx - 조회할 몰입 세션의 고유 식별자(ID)
//   imm_idx를 URL 파라미터로 전달하여 해당 세션의 타임랩스만 필터링합니다.
// 반환 예시 : [{ timelapse_idx: 7, file_name: 'timelapse_20260416_093000.mp4', created_at: '2026-04-16' }, ...]
export const getTimelapsesAPI = (imm_idx) =>
  axiosInstance.get(`/timelapses/${imm_idx}`);
