/*
  JWT(JSON Web Token)란?
  로그인한 사용자를 서버가 기억하는 방식 중 하나입니다.
  로그인에 성공하면 서버가 "이 사람은 인증된 사용자야"라는 정보를
  암호화된 토큰(문자열)으로 만들어 클라이언트에게 전달합니다.
  이후 클라이언트는 요청 시마다 이 토큰을 함께 보내고,
  서버는 토큰을 검증해 별도의 DB 조회 없이 사용자를 식별합니다.

  JWT의 구조 : xxxxx.yyyyy.zzzzz (점(.)으로 구분된 3개의 파트)
    - Header  (xxxxx) : 토큰 타입과 암호화 알고리즘 정보
    - Payload (yyyyy) : 실제 담긴 데이터 (user_idx, nick 등 사용자 정보)
    - Signature(zzzzz) : Header + Payload를 비밀 키로 서명한 값 (위변조 방지)


  액세스 토큰(Access Token) vs 리프레시 토큰(Refresh Token)이란?
  토큰을 하나만 쓰면 탈취 시 만료될 때까지 막을 방법이 없습니다.
  그래서 두 가지 토큰을 함께 사용하는 전략을 씁니다.

    ┌─────────────────┬──────────────────────────────────────────────┐
    │  액세스 토큰    │ 실제 API 요청에 사용하는 토큰                │
    │  (Access Token) │ 유효 시간이 짧아 탈취되어도 피해가 제한됨   │
    ├─────────────────┼──────────────────────────────────────────────┤
    │  리프레시 토큰  │ 액세스 토큰이 만료됐을 때 재발급받는 토큰   │
    │ (Refresh Token) │ 유효 시간이 길고 안전한 곳에 보관해야 함    │
    └─────────────────┴──────────────────────────────────────────────┘
*/


// ────────────────────────────────────────────────
// ⚙️ JWT 설정값 객체 내보내기
// ────────────────────────────────────────────────

module.exports = {

    // ── 액세스 토큰 서명 키 ──────────────────────────
    /*
      서버가 토큰을 만들고 검증할 때 사용하는 비밀 키입니다.
      이 키가 있어야 토큰의 서명(Signature)을 생성하고 유효성을 확인할 수 있습니다.
      키가 외부에 유출되면 누구든 유효한 토큰을 위조할 수 있으므로
      반드시 .env 파일에 길고 복잡한 값으로 설정해야 합니다.

      ⚠️ 'CHANGE_ME_IN_ENV'는 기본값(fallback)일 뿐입니다.
         실제 운영 환경에서는 반드시 환경 변수로 교체해야 합니다.
    */
    secret: process.env.JWT_SECRET || 'CHANGE_ME_IN_ENV',


    // ── 액세스 토큰 만료 시간 ────────────────────────
    /*
      액세스 토큰이 유효한 시간입니다. 이 시간이 지나면 토큰이 만료되어
      API 요청 시 인증 오류가 발생하고, 리프레시 토큰으로 재발급받아야 합니다.

      시간 표기 형식 예시:
        '2h'  → 2시간  (현재 설정)
        '15m' → 15분
        '1d'  → 1일

      짧을수록 보안에 유리하지만 사용자가 자주 재로그인해야 하는 불편함이 있으므로
      서비스 특성에 맞게 적절한 값으로 설정합니다.
    */
    expiresIn: process.env.JWT_EXPIRES_IN || '2h',


    // ── 리프레시 토큰 서명 키 ────────────────────────
    /*
      리프레시 토큰 전용 비밀 키입니다.
      액세스 토큰의 서명 키(secret)와 반드시 다른 값으로 설정해야 합니다.
      같은 키를 사용하면 리프레시 토큰을 액세스 토큰처럼 위조하여
      사용하는 보안 취약점이 생길 수 있습니다.

      ⚠️ 'CHANGE_REFRESH_ME_IN_ENV'는 기본값(fallback)일 뿐입니다.
         실제 운영 환경에서는 반드시 환경 변수로 교체해야 합니다.
    */
    refreshSecret: process.env.JWT_REFRESH_SECRET || 'CHANGE_REFRESH_ME_IN_ENV',


    // ── 리프레시 토큰 만료 시간 ──────────────────────
    /*
      리프레시 토큰이 유효한 시간입니다.
      이 시간이 지나면 리프레시 토큰도 만료되어 사용자가 다시 로그인해야 합니다.
      액세스 토큰보다 훨씬 길게 설정하는 것이 일반적입니다.

      시간 표기 형식 예시:
        '7d'  → 7일  (현재 설정)
        '30d' → 30일
        '1y'  → 1년

      리프레시 토큰은 유효 시간이 길기 때문에 탈취될 경우 피해가 크므로
      HttpOnly 쿠키 등 안전한 방식으로 보관해야 합니다.
    */
    refreshExpiresIn: process.env.JWT_REFRESH_EXPIRES_IN || '7d',
};
