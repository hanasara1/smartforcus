// ─────────────────────────────────────────────────────────
// server/src/middlewares/validate.middleware.js
// express-validator 결과를 일괄 처리하는 미들웨어
// ─────────────────────────────────────────────────────────


// ────────────────────────────────────────────────
// 📦 필요한 모듈 불러오기
// ────────────────────────────────────────────────

/*
  express-validator : 요청 바디, 파라미터, 쿼리 등의 입력값을 검증하는 라이브러리입니다.
  validationResult  : 라우터에 등록된 검증 규칙(체인)들의 실행 결과를 수집하는 함수입니다.
                      검증 오류가 있으면 오류 목록을 반환하고, 없으면 빈 결과를 반환합니다.
*/
const { validationResult } = require('express-validator');


// ────────────────────────────────────────────────
// ✅ 입력값 유효성 검사 미들웨어
// ────────────────────────────────────────────────

/*
  validate

  [역할]
  라우터에 등록된 express-validator 검증 규칙들의 실행 결과를 수집하고,
  오류가 하나라도 있으면 컨트롤러로 넘어가지 않고 422 응답을 반환합니다.
  모든 오류를 한 번에 모아 반환하므로 클라이언트가 어떤 필드가 왜 잘못됐는지 한눈에 파악할 수 있습니다.

  ▼ 사용 방법 ▼
    라우터에서 검증 규칙 체인들 뒤에 이 미들웨어를 등록합니다.
    검증 규칙들이 먼저 실행되어 오류를 req 객체에 누적시키고,
    이 미들웨어가 그 결과를 수집하여 일괄 처리합니다.

    예시:
      router.post('/register',
        body('email').isEmail(),       // 검증 규칙 ①
        body('pwd').isLength({ min: 6 }), // 검증 규칙 ②
        validate,                      // 결과 일괄 처리
        register                       // 컨트롤러
      );

  ▼ errors.array() 구조 ▼
    각 오류 객체는 아래 형태를 가집니다.
      { path: 'email', msg: '유효한 이메일을 입력해주세요.', ... }
    map으로 필요한 필드(field, message)만 추출하여 간결한 형태로 반환합니다.

  ▼ 422 Unprocessable Entity란? ▼
    요청 형식은 올바르지만 입력값의 의미적 유효성 검사에 실패했을 때 사용하는 HTTP 상태 코드입니다.
    (예: 이메일 형식 오류, 비밀번호 길이 부족 등)

  [처리 순서]
    1. validationResult(req)로 누적된 검증 오류를 수집합니다.
    2. 오류가 있으면 422와 함께 오류 목록을 반환합니다.
    3. 오류가 없으면 next()로 다음 미들웨어 또는 컨트롤러에 흐름을 전달합니다.

  @returns 422 : 유효성 검사 실패 + 오류 목록 [{ field, message }]
  @calls   next() : 유효성 검사 통과 시 다음 미들웨어 또는 컨트롤러로 전달
*/
const validate = (req, res, next) => {

  // 라우터에 등록된 검증 규칙들의 실행 결과를 수집합니다
  const errors = validationResult(req);

  // ── 유효성 검사 실패 처리 ────────────────────────────
  if (!errors.isEmpty()) {
    return res.status(422).json({
      success : false,
      message : '입력값 유효성 검사 실패',
      /*
        errors.array() : 수집된 오류 목록을 배열로 변환합니다.
        map으로 각 오류에서 필드명(path → field)과 메시지(msg → message)만 추출합니다.
        클라이언트는 이 배열을 보고 어떤 필드에 어떤 문제가 있는지 파악할 수 있습니다.
        (예: [{ field: 'email', message: '유효한 이메일을 입력해주세요.' }])
      */
      errors  : errors.array().map((e) => ({ field: e.path, message: e.msg })),
    });
  }

  // 모든 검증을 통과하면 다음 미들웨어 또는 컨트롤러로 흐름을 전달합니다
  next();
};


// ────────────────────────────────────────────────
// 📤 외부로 내보내기
// ────────────────────────────────────────────────

/*
  module.exports : 이 파일의 함수들을 다른 파일에서 require()로 사용할 수 있게 합니다.
    - validate : 유효성 검사 규칙 체인 뒤, 컨트롤러 앞에 미들웨어로 등록
*/
module.exports = { validate };
